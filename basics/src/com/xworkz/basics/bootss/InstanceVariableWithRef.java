package com.xworkz.basics.bootss;

public class InstanceVariableWithRef {

	private String name="shruti";
	private int age=22;
	
	public static void main(String[] args) {
		InstanceVariableWithRef insref=new InstanceVariableWithRef();
		System.out.println(insref.name);
		System.out.println(insref.age);
	}
	
	
}
